function menu1(){
    let noticeRef = document.getElementById("notice1");
    let foodRef = document.getElementById("food");
    let drinkRef = document.getElementById("drink");
    let dessertRef = document.getElementById("dessert");
    let branchRef = document.getElementById("branch");
    noticeRef.style.display = "none";
    drinkRef.style.display = "none";
    dessertRef.style.display = "none";
    branchRef.style.display = "none";
    if (foodRef.style.display == "none") {
        foodRef.style.display = "block";
    }
   
}
